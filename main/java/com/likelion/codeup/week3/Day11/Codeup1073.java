package com.likelion.codeup.week3.Day11;
import java.util.Scanner;

public class Codeup1073 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();

        while(num != 0) {
            System.out.println(num);
            num = sc.nextInt();
        }
    }
}